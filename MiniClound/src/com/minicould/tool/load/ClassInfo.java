package com.minicould.tool.load;

class ClassInfo {

	private String name;
	
	private byte[] byteArray;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public byte[] getByteArray() {
		return byteArray;
	}

	public void setByteArray(byte[] byteArray) {
		this.byteArray = byteArray;
	}
	
	
}
